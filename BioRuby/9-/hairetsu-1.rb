### File-name: hairetsu-1.rb ###

hairetsu_1 = ['abc', 'def', 'ghi', 'jkl',
							'mno', 'pqr', 'stu', 'vwx', 'yz']		# 配列は[(配列名) = [] ]で表す

p hairetsu_1[0]		#=> "abc"

p hairetsu_1[1]		#=> "def"

p hairetsu_1[-1]	#=> "yz"			# 配列の要素は[(配列名)[数字] ]で表す

p hairetsu_1[-2]	#=> "vwx"			# [-1]は一番最後のデータ、[-2]は最後から2番目のデータを表す
